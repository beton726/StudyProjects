package Yusov.spaceGame;

public abstract class BaseObject {

    private double x;
    private double y;
    private double radius;

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }




}
